  	<b class="text2Bold">DOMINGO 8 de junio</b><br><br>
<b>11:00</b> El Mercado se dispone a recibir el día<br><br> 

<b>11:00</b> Los músicos recorren el mercado al compás de sus sones<br><br>

<b>11:30</b> Durante otra de las jornadas, el ocio vendrá dado por los bailes, los más corteses y
         también los mas populares, en una escena que recrea los gratos momentos de ocio en
        "La pradera de San Isidro" y que, por tratarse de La Granja, nosotros bien la podría-mos
          subtitular "La Pradera de San Ildefonso"<br><br>

<b>12:30</b>  Apertura de Talleres de oficios perdidos<br><br>

<b>13:00</b>  Las Danzas Boleras del Real Sitio, amenizan la mañana<br><br>

<b>13:30</b>  Exhibición de Aves Rapaces     <br><br>

<b>14:30</b> Descanso para  bien alimentarse<br><br>

<b>17:30</b>  El Mercado se dispone a abrir sus paradas<br><br>

<b>18:00</b> Por la tarde, como es de rigor y como bien anuncian los pasquines: "Tarde de toros". Recreación satírica de una corrida de toros, llena de maleantes, farsantes, petimetres y el toro; por supuesto, aunque con trazo de "aguafuerte" (que somos osados, pero  no valientes)<br><br>

<b>18:30</b>  Apertura de Talleres de oficios perdidos<br><br>

<b>19:00</b>  Demostración de vuelo de Cetrería.     <br><br>

<b>19:30</b> Animación de la trouppe interpretando personajes de la época.<br><br>

<b>20:00</b> La Joven Camerata del Real Sitio nos trae diversas piezas de música barroca.<br><br>

<b>21:00</b> Los músicos recorren el mercado al compás de sus sones.<br><br>

<b>22:00</b> El mercado llega a su fin, con el espectáculo "Caprichos"<br><br>

La segunda noche se viste de clarooscuro, penetrantes trazos sometidos al papel mojado. Así, presentamos un espectáculo diseñado al "aguafuerte", poblado de Caprichos.<br><br>

La paradoja del creador frente a su criatura, el placer de una vida alegre y mundana sin un tarugo de pan, el amor embozado, la soledad de la ilusión, la locura de los más cuerdos, la arrogancia de los infieles…y  todo ello a través de un sueño. "El sueño de la razón"<br><br>
 
El universo del Genio renace entorno a sus personajes en blanco y negro, cargados de dinamismo y paradojas y por ello enfrentados a un tumulto de contradicciones que los trasladará al límite de lo <i>"horizontalmente dispuesto"</i>.<br><br>

<i>"…Allá va eso que algún partido saca a quién amarga presencia…"</i><br><br>


