  	<b class="text2Bold">PREAMBULOS</b><br><br>
  A ritmo de tonadilla y tras doscientos años de historia; presentamos un nuevo modelo de mercado más 
  "pintoresco" y exclusivo.<br><br>

  Pintoresco en lo que a su estética se refiere ya  que en efecto, tomamos la obra pictórica de  don 
  Francisco de Goya y Lucientes como referencia inmediata. A los lienzos  del  maestro sumaremos a otros
  grandes de la época como son sus artistas coetá-neos Bayeu y  Maella.<br><br>

  Sus obras son el testigo indiscutible de una sociedad que recrearemos de manera desenfadada; así, 
  aguadoras, lavanderas, toreros, embozados, pastores, floreras y demás,  se darán cita en las calles, 
  recordando los chismes, usos y costumbres del momento.<br><br>

  Exclusivo por el fiel reflejo de un fenómeno social del momento, el del <i>"majismo"</i> y que pronto se 
  definiría como goyesco, esto nos permite un doble juego frente a los personajes a interpretar: pueblo 
  y aristocracia se confunden.<br><br>

  Como bien nos cuentan Menéndez Pidal y Díaz Paja; la aristocracia toma como juego social el 
  "disfrazarse" de pueblo, asumiendo además sus roles de comportamiento. <br><br>

  Pero también es exclusivo por el momento histórico que atraviesa España. Doble cara de la misma 
  moneda; durante el día pervive una sociedad dinámica y alegre, mientras que la noche  muestra  otra 
  realidad
</div>

  <div id="texto_main">
  <b class="text2Bold">SABADO. 7 de junio</b><br><br>
	<i>Despertaba el día y albor primero<br>
	Con sus mil ruidos<br>
	Despertaba el pueblo…</i><br><br>

	Al bullicio de artesanos, oficiantes y mercaderes, se suman una serie de personajes 
	"pintorescos", una sociedad, alegre y extrovertida, llena de luz y color. <br><br>

<b>11:00</b> El Mercado de la Granja de San Ildefonso abre sus puertas<br><br>

<b>12:30</b> Tras el paseo del cortejo en el que se entremezclan la aristocracia y el pueblo llano; llega el momento de anunciar el comienzo de los festejos
Desde la puerta Reina llega hasta la puerta del Ayuntamiento, una carroza real ti-rada por cuatro caballos, que nos traen autoridades de hoy y de ayer. Todos ellos en co-mitiva, suben al escenario, y nos disponemos a oír "El Pregón".
La ceremonia comienza con todo su boato y rigurosidad…pero algo curioso va a ocurrir, no olvidéis que aristocracia y pueblo se confunden…<br><br>

<b>13:30</b>  Gran manteo de peleles: se invitará al público a mantear según marcan las reglas del juego (En diferentes puntos, diferentes grupos a la vez mantearan un pelele) 
Esto nos lleva a recrear escenas de carácter lúdico, en las que nuestros personajes buscarán una participación más activa por parte del público.<br>
Quien llegara mas alto?!!...<br><br>



   <b>15:00</b> Descanso para el bien alimentarse<br><br>

<b>17:30</b>  El Mercado se dispone a recibir a los vecinos<br><br>

<b>18:00</b> Habrá que  aprovechar los últimos rayos de sol primaverales y para ello, na-da más satisfactorio que unos bailes entre cortesanos y populares. Requerimos, una vez más, de la colaboración del público; así se entremezclan "Matelot" con "La Diligenta" o "El Pasacalles de Frias" con "La danza de San Juan", o "El Zángano", entre otros.<br><br>

Y entre tanto Alboroto y tanta algarabía, aparecerán otros paseantes… pequeñas pinceladas de aquello que Goya una vez recreara:<br>
Una escena de coqueteo exquisito, galante y cortés, como es el cartón titulado "El parasol". Ella pasea su porte con fina arrogancia y él la protege de los rayos solares con un parasol y un sin fin de madrigales. Comienza el cortejo.<br>
De otra parte, "Las cantareras" harán las delicias del respetable con sus chismes y devaneos de villanas.<br><br>

"El cacharrero" nos abruma con su retahíla de ollas, cuencos y aceiteras; tal vez haga negocio con la más lozana de las mozas. Que poco dista el negocio del ocio…<br><br>
	
Entre gritos, patrañas y flirteos; majas, villanos, embozados y un sin fin más de personajes acompañaran las calidas jornadas de La Granja.<br><br>

<b>21:30</b> Cerraremos la primera jornada con el espectáculo "JUEGOS Y FUEGOS DE AMOR"<br><br>

Desde la Puerta de la Reina llegará hasta el espacio central en un animado pasaca-lles,  en donde los enamorados recitarán al oído de sus damas sus cuitas del corazón sin saber que un pícaro duende guiado por una traviesa hada les apresará en un cómico enredo.<br><br>


<b class="text2Bold">ESPECTACULO DEL SABADO NOCHE.</b><br><br>
<b>El Gran Circo del Mar:<br>
Oceánides Enamorado</b><br><br>

Convertiremos a la Plaza del Ayuntamiento en una gran pista de Circo donde fachadas, adoquines y viandantes quedarán sumergidos en teatral fondo del mar. Recuperando así una de las tradiciones más longevas de nuestra historia: El Circo. En el que se entremezclan malabares, danza vertical, lucha de espadas, todo ellos acompañado de grandes efectos luminotécnicos apoyados a su vez, por pequeños detalles de pirotecnia  <br><br>

	Esta primera noche, trae a nuestro recuerdo una de tantas historias que en más de una ocasión amenizaran las largas puestas de sol en Palacio. Era la mitología uno de los temas predilectos en la Corte y por ello queremos evocar alguno de estos relatos.<br><br>
	
La fábula que a continuación os narraremos fue escogida por La Granja; el encanto de su arquitectura nos pedía una historia no de dioses poderosos sino humanos; los parajes mágicos que la rodean nos sugería una historia de amor y el seductor repicar de sus aguas nos otorgaba una historia sobre el agua. Así nació "OCEÁNIDES ENAMORADO"<br><br>

El mar está embravecido; no cesan las tormentas, los peces viven sumergidos en las cavernas más recónditas y los barcos no se atreven a izar sus velas. Neptuno, el dios más poderoso, el más bravo y osado, ama sin ser amado.<br><br>
	Anfítrite es la causa de su desamparo pues como cuenta la leyenda, ella buscó refugio en Atlas al verse cortejada por aquel dios grandilocuente y descomunal. Muchos fueron los emisarios que trataron de convencerla pero sólo uno de ellos consiguió sus favores para con el Dios. Delfino es su nombre y fue su valor  lo que le permitió llegar hasta ella y su humildad lo que le llevó a mostrarle a tan hermosa Nereida el corazón arrogante pero sincero del gran Neptuno.	<br><br>

	A diferencia de otras historias de amor, ésta tuvo un final feliz que hoy en día vemos reflejado en el cielo y que el Dios regalara en aquella ocasión a Delfino (o delfín). Nos referimos a la constelación del delfín.<br><br>
  
