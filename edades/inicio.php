Son muchas las maneras de visitar La Granja: unos vienen por contemplar las fuentes, los jardines, 
la Colegiata y el palacio; otros se acercan para visitar la fábrica de cristal, el museo del vidrio, las 
salas de exposiciones, y para disfrutar de su entorno. Tanto unos como otros, después de admirar y llevarse 
grabada en la retina de sus ojos y en el celuloide de sus cámaras video-fotográficas parte del legado 
artístico cultural que nos dejaron nuestros antepasados, vuelven a su lugar de origen satisfechos de todo 
lo que han visto, pero desconocedores de otras maravillas que existen.<br><br>

La Granja, desde sus orígenes, desarrolló su convivencia en torno al Hecho Religioso y lo expresó en formas 
de vida "comunitaria" con un gran sentido religioso-social, naciendo de él las hermandades y cofradías:<br><br>
<div style="padding-left:40px; padding-right:100px">
	"Cristo del Perdón", "Virgen de la Soledad", "Virgen de los Dolores", "San Ildefonso", "San Roque", 
  "San Juan Nepomuceno", "San Isidro", "El Patrocinio", "Orden de Terciarios Franciscanos", "Animas", 
  y más reciente, "Cristo de los Alijares".
</div><br><br>  

Cada una de ellas con sus peculiaridades, sus fiestas, sus imágenes y, ante todo, al hacerse presente en las
necesidades de los hermanos y cofrades, "lograban" sus fines.<br><br>

Si bien muchas han desaparecido, otras han quedado recordándonos sus mejores tiempos y dejándonos su 
presencia hecha orfebrería, pintura, escultura, tapicería, etc....<br><br>

Mención aparte merecen la Insigne y Real Colegiata Parroquial de la Santísima Trinidad, su Abadía y su 
Cabildo.<br><br>

El 20 de Diciembre de 1724, el Papa Benedicto XIII expedía en Roma la Bula de erección de la Colegiata 
"Dum Infatigabilem", con categoría de sede Episcopal y la dotaba de un Abad con jurisdicción "quasi episcopal" 
sobre un territorio que comprendía:<br><br>
<div style="padding-left:40px; padding-right:100px">
San Ildefonso, Valsaín, Revenga, Las Navas de Riofrío, Trescasas, Sonsoto, Palazuelos, Tabanera, Ntra. Sra. del Robledo y Santa Cecilia.
</div><br><br>  

El 23 de Enero de 1874, siguiendo las directrices dadas por la Bula "Qua Diversas", expedida en Roma por el 
Papa Pío IX, en Julio de 1873, se dictaba el correspondiente `Auto de Supresión" de todas las Abadías 
"quasi episcopales", quedando vinculadas a las diócesis correspondientes.<br><br>

La presencia espiritual, asistencial y cultural de la Colegiata y su Cabildo por medio de los Abades-Obispos
 (16) y sus cientos de Canónigos fue grande en el período entre estas dos fechas (150 años), dando lugar a 
 esplendor religioso, litúrgico, culturaL.., como lo testimonian los legados que nos trasmitieron y que como 
 testigos silenciosos tinas veces, y hablándonos al interior del corazón otras muchas, han estado y están 
 presentes en nuestra comunidad cristiana.
