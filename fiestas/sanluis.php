 <IMG src="images/fiestas/toro_fuego.jpg" border="1" width="250" height="188" id="ImgRight" align="right">
 La semana del 25 de Agosto, La Granja se viste de fiesta. <br>
 La alegr&iacute;a invaden nuestras calles y el pueblo entero estalla en risas y colores, rompiendo 
 el monótono curso del año y en donde se nos presenta la ocasión, no sólo de honrar a nuestro patrón, 
 sino de compartir con vecinos y forasteros, el placer de unos días festivos ganados con el pulso 
 de nuestro trabajo a lo largo de todo el año. <br><br>
 
 Se puede decir que vivimos por y para las fiestas de San Luis. <br><br>
 
 En estas fiestas, hoy como ayer, los chiquillos siguen corriendo delante y detrás de los cabezudos, 
 buscando las varas de los cohetes y esquivando las zambombas, la banda de música toca diana y anuncia 
 alegría en un pasacalles animado, los nuevos titiriteros nos enseñan su mundo de habilidad e ingenio. 
 Toros, verbena, teatro, fuegos artificiales, gastronom&iacute;a, feria ... <br><br>
 
 Todo se olvida en Fiestas, se olvidan las discusiones, las labores diarias se paralizan, cierran 
 las industrias y los comercios, los niños llevan en sus bolsillos más euros que en el resto del año, 
 las mujeres están más guapas que nunca, y la noche envuelve a una multitud humana que no cabe de pies. 
 Todo el pueblo es una feria, en el que se dan cita mayores, jóvenes y niños de muchos lugares. 
 Hay espectáculo para todos, en el que cada cual tiene su cometido, como un complejo puzzle en el que todo 
 encaja a la perfección. Son varios días intensos que se estiran cuanto se puede, buscando perpetuar 
 ese ambiente especial que lo envuelve todo, donde las pe&ntilde;as, en su mayoria juveniles, toman 
 las calles con su alegria y colorido.<br><br>
 
 En época de fiestas, el pueblo se convierte en lugar de encuentros: Familiares y amigos que vienen de 
 lejos a pasar unos días son recibidos por los que viven en La Granja todo el año.<br>
 
 Ha llegado el momento para el disfrute, para compartir alegría y risas con nuestros seres queridos, 
 para brindar porque un año más estamos aquí todos juntos celebrando el comienzo de las fiestas 
 de San Luis. Que no falte la felicidad en todas y cada una de las casas de la Granja en estos días. <br><br>
 <b>Felices Fiestas a todos</b>. <br>
