<div id="texto_main"> 
  <span class="text2Bold">Sábado 2 de Junio</span><br><br>
	<IMG src="images/fiestas/portadacartel.jpg" border="1" width="200" height="245" id="ImgRight" align="right">
	<b>11:00 h.</b> El Mercado, ya dispuesto, nos invita a acercarnos, curiosos, atraídos por tan diferentes artesanías. <br><br>
	<b>12:00 h.</b> Paseo de damas y caballeros acompañados por los músicos de la corte. Juegos de Damas, La gallinita ciega, el pelele y 
	la herradura. Juegos de caballeros, el picón y los bolos. Invitando al público en tan gustosas prácticas. <br><br>
	<b>13:30 h.</b> Pregón Real: las Autoridades hacen su entrada en la Calesa Real, los cortesanos les acompañan. 
	Una pintoresca juglaresca con su alboroto les sigue con alegría, todos acompañan para inaugurar este Real Mercado.
	Exhibición de Cetrería. 
	Todos animan y festejan esta mañana calurosa ante los asistentes. <br><br>
	<b>14:00 h.</b> Una alcahueta hace hechizos de amor. Un sumiller de corte nos recrea con sus chismes. 
	Un hidalgo enamorado recita bellos versos. 
	Ana Pantoja, tabernera de la Plaza de la Leña, nos descubrirá algunas de sus recetas.<br><br>
	<b>15:00 h.</b> El mercado descansa. <br><br>
	<b>18:00 h.</b> El mercado cobra vida, artesanos y mercaderes preparan sus mercancías para vender a los lugareños. <br><br>
	<b>19:00 h.</b> Exhibición de cetrería y exhibición de oficios ya olvidados.
	Tarde de mentidero. Desde el atrio asoman los hidalgos caballeros y alguna que otra dama, que dan cuenta de lo acontecido en la Villa. 
	La desdichada Marizápalos nos refiere sus amores. 
	Dos Celestinas salen a la plaza: gran discusión. 
	Escuela de espada, dos ilustres espadachines enseñan su manejo con destreza. 
	Los habilidosos maestros se baten en ilustrada demostración. 
	Personajes del pueblo inundan el mercado, pícaros, pedigüeños, etc… Los músicos amenizan. <br><br>
	<b>22:00 h.</b> MOJIGANGA: Divertida mascarada donde cortesanos y transeúntes atrevidos, unidos en rondas, danzarán al son de la jerigonza. <br><br>
	<b>24:00 h.</b> En el escenario se desarrolla el espectáculo de música, luz y fuego “Ornato y Perfume en una Noche Barroca” 
