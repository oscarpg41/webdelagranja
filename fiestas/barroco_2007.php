<div id="texto_main"> 
  <span class="text2Bold">Domingo 3 de Junio</span><br><br>
	<IMG src="images/fotos/110/mercado_barroco_2006_1.jpg" border="1" width="310" height="233" id="ImgRight" align="right"> 
	<b>11:30 h.</b> El mercado cobra vida. <br><br>
	<b>12:00 h.</b> Los músicos recorren el mercado, haciendo danzar y saltar al pueblo. Los cómicos toman el mercado con sus personajes de 
	ambientación.<br>
	Paseo de damas y caballeros aprovechando así la mañana para el galanteo, acompañados por los músicos de la corte. <br>
	Juegos de Damas, La gallinita ciega, el pelele y la herradura. Juegos de caballeros, el picón y los bolos. Invitando al público en tan gustosas prácticas. <br><br>
	<b>12:30 h.</b> Exhibición de cetrería y exhibición de oficios ya olvidados. <br><br>
	<b>13:00 h.</b> Los cómicos interpretan un entretenido divertimento de Lope de Rueda: “Cornudo y Contento”. <br><br>
	<b>13:30 h.</b> Las Danzas Boleras del Real Sitio nos descubrirán esta elegante coreografía. Mientras, divertidos personajes nos acompañarán en el final de esta mañana. <br><br>
	<b>14:30 h.</b> El mercado descansa. <br><br>
	<b>18:00 h.</b> El mercado cobra vida, artesanos y mercaderes preparan sus mercancías para ponerlas al servicio de los lugareños. <br><br>
	<b>18:30 h - 21:30 h.</b> Acciones y animaciones teatrales por el mercado. 
	Una alcahueta y Fray Joan discuten sobre los placeres de la carne.  
	Dulces melodías recorren el mercado de la mano de los músicos de la Corte.  
	Exhibición de cetrería y exhibición de oficios ya olvidados.  
	Juan de Osuna, cómico enamorado, desvela su amor por la Calderota.  
	Dos damas de postín se disputan el amor de un petimetre. 
	Joven Camerata del Real Sitio ameniza la plaza con piezas escogidas de repertorio barroco. 
	Las Danzas Boleras del Real Sitio nos descubrirán esta elegante coreografía. 
	Escaramuzas entre espadachines. Los músicos entretienen a los paseantes. <br><br>
	<b>22:00 h.</b> “Las cuatro afrentas del amor”: los cuatro dioses del amor acuden bajo palio. Pasacalle que finalizará en una alegre mascarada. Espectáculo con fuego, pirotecnia y música.
