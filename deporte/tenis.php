<b>INFORMACIÓN GENERAL</b><br><br>
La Escuela Municipal de Tenis, se encuentra engloba en las Escuela Deportivas del municipio, en donde a largo de 
varios  años de trabajo se ha conseguido que cerca de 80 alumnos aprendan y practiquen este apasiónate deporte.<br><br>

Es una oferta deportiva más que ofrece este municipio, y que abarca a una extensa población, independientemente de la edad. <br><br>

<img src="images/fotos/98/escuela_tenis_lagranja_5.jpg" id="imgRight" align="right">Dentro de la filosofía de una participación para todos, hemos conseguido que La Escuela de Tenis se consolide cada año, gracias sobre
todo al apoyo del Ayuntamiento de la Granja y Valsaín. <br><br>

El Club de Tenis y de Padel se puso en funcionamiento en el año 2006 de la mano de varios vecinos del municipio, 
con el interés común, de la práctica de este apasionante deporte.<br><br>

El Club cuenta actualmente con 32 socios y abierto a toda la población que desee participar en la práctica del 
Tenis. Dentro de sus funciones está la de promover, dirigir y organizar el Tenis en nuestro municipio y que junto 
con las Escuelas Municipales de Tenis se está llevando a cabo en estos últimos años, contando  actualmente  con 
más 80 alumnos/as.<br><br>

Uno de los pasos más importantes que ha dado el Club en este año ha sido la de formar parte de la Federación 
Nacional de Tenis y la Federación Territorial de Tenis de Castilla y León  como Club Federado. Esto implica que 
el Club asuma una serie de compromisos, tanto administrativos como económicos.<br><br>

Los objetivos del Club son la promoción, dirección y organización del Tenis en la localidad. Relaciones sociales
entre los miembros del Club. Formación y participación de equipos de todas las categorías en Campeonatos y Ligas 
a nivel Nacional, Regional, Provincial o Local. Ayuda a jugadores destacados para su formación sea más 
especializada. Organización de torneos a nivel: Social, Local, Provincial, Autonómico y Nacional, Captación de 
jugadores para la escuela y  el  Club de tenis.<br><br>

Con estos criterios y junto con el trabajo que se viene haciendo desde hacía varios años en  la Escuela 
Municipales de Tenis de la Granja, se pretende que los alumnos con mayor proyección tengan la posibilidad de 
participar  de forma Federada en todas las competiciones organizadas por este órgano, por tanto se pretende que 
los  niños que participen en competiciones se encuentren respaldados y apoyados por Administración local, el 
club, patrocinadores, etc,...

