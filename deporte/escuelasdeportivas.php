En 1992, la Excma. Diputaci&oacute;n de Segovia cre&oacute; las Escuelas Deportivas Municipales
en nuestra provincia, a trav&eacute;s de un convenio de colaboraci&oacute;n en el que se implicaba a 
los Ayuntamientos de las localidades afectadas y a CajaSegovia. <br><br>

La oficina de las Escuelas Municipales en San Ildefonso se encuentra en el Pabell&oacute;n Municipal
de Deportes y est&aacute; coordinada por Juan Garc&iacute;a Cocero, quien lleva desempe&ntilde;ando estas funciones 
desde 1992. <br><br>

Uno de los objetivos, quiz&aacute;s el primordial, es el de educar a los ni&ntilde;os en sociedad, fuera de
entornos identificados con la disciplina (como puede ser el colegio). <br><br>

Un ni&ntilde;o que haya estado en las Escuelas Municipales tendr&aacute; m&aacute;s cuidado con su higiene personal,
m&aacute;s respecto a la propiedad ajena, m&aacute;s responsabilidades frente a sus compromisos y un c&iacute;rculo
de amistades m&aacute;s amplio y cercano. <br><br>

Las competencias de las Escuelas Deportivas se reparten entre Diputaci&oacute;n y Ayuntamiento, con la
participaci&oacute;n de CajaSegovia y otras entidades como las diversas federaciones deportivas. <br><br>

Los calendarios de las actividades suelen coincidir m&aacute;s o menos con el calendario escolar, 
inici&aacute;ndose en Octubre y ext&eacute;ndiendose hasta finales de Mayo. <br>
Durante el curso 06/07, las Escuelas han contado con la asistencia de 400 ni&ntilde;os y ni&ntilde;as de entre 
6 y 16 a&ntilde;os, pudiendo variar la edad según la modalidad deportiva., coordinados por 20 
monitores titulados para cada disciplina que se ha impartido.<br><br>

La organizaci&oacute;n y desempe&ntilde;o de las diferentes &aacute;reas son participadas por la Excma. Diputaci&oacute;n 
de Segovia en lo que se refiere a log&iacute;stica, material, transporte y supervisi&oacute;n. El Excmo. 
Ayuntamiento de La Granja coordina el desarrollo, presta las instalaciones y se encarga de 
que cada ni&ntilde;o o ni&ntilde;a pase un reconocimiento m&eacute;dico obligatorio para evitar incompatibilidades
entre la salud y las exigencias f&iacute;sicas. CajaSegovia desembolsa el 50% de los honorarios de los
monitores deportivos. Adem&aacute;s hay empresas privadas con las que se negocia un servicio especial
para las Escuelas, tal es el caso de H&iacute;pica Eresma con quienes se ha impartido los cursos de 
equitaci&oacute;n, el C.D. La Granja, que ha colaborado en las particiones de los "juegos de edad" y 
ha facilitado la inscripci&oacute;n de los participantes en la Federaci&oacute;n de F&uacute;tbol de Castilla y Le&oacute;n.<br>
Tambi&eacute;n el grupo de monta&ntilde;a La Acebeda, que cuenta con monitores titulados, tiene relaci&oacute;n
con las Escuelas Deportivas: adem&aacute;s de realizar marchas y acampadas, tienen cedido el roc&oacute;dromo 
situado en el Pabell&oacute;n de Deportes, o mejor dicho, el espacio que ocupa el roc&oacute;dromo, ya que son ellos
quienes lo han dise&ntilde;ado, construido y lo mantienen. <br><br>

Las cuotas de inscripci&oacute;n para cada ni&ntilde;o, en el curso 06/07 fueron de 36 euros por deporte y ni&ntilde;o.
La H&iacute;pica es una excepci&oacute;n en cuanto al precio ya que por sus caracter&iacute;sticas debe de hacerse un pago 
mensual de 30 euros. <br> 
C&oacute;mo &uacute;ltima excepci&oacute;n, la Psicomotricidad cuesta 12 Euros al mes.<br>
Puedes informarte m&aacute;s ampliamente en el Ayuntamiento de San Ildefonso-La Granja.  <br><br>

