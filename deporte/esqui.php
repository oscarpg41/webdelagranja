  <B>Breve Historia de la Estación:</B><br><br>

Como es sobradamente conocido, la Estación de Esquí de Puerto de Navacerrada se encuentra en los términos municipales de Cercedilla  y Navacerrada, en la Comunidad de Madrid, y La Granja de San Ildefonso en la Comunidad de Castilla-León.
Se puede considerar como fecha de inicio de la práctica de los Deportes de Invierno en el Puerto de Navacerrada el año 1903, fundándose el primer Club de Esquí en 1907. <br><br>

El 9 de octubre de 1954 se crea Transportes Aéreos del Guadarrama, S.A. (TAGSA) que instala el primer Telesilla en 1955: el Telesilla de Guarramillas. <br><br>

La Estación de Esquí de Puerto de Navacerrada es la precursora en España de los cursos de formación de profesores y en ella se probó en 1960 el primer sistema de producción de nieve del País.<br><br>


Ya desde los años 50 la afluencia de público era masiva, llegando a su máximo apogeo en la décadas de los 60 y 70. En algunos fines de  semana y festivos se llegaron a contabilizar hasta 130 autobuses y 7.800 vehículos ligeros, con una afluencia estimada, en base a la ocupación de los vehículos, de más de 40.000 personas.<br><br>
 
A principios de los 70, la Estación de Esquí de Puerto de Navacerrada contaba con 10 remontes y 13 pistas de esquí, además de un circuito de esquí de fondo y un trampolín de saltos, en total, más de 12 kilómetros de pistas.<br><br>

Cabe destacar que la Estación de Esquí de Puerto de Navacerrada es víctima de su propio éxito, la Estación es pionera en cuanto a instalaciones, pero la afluencia masiva de esquiadores por la cercanía de la Estación a Madrid, implica que éstas queden obsoletas en un corto periodo de tiempo. Lo anterior se ve agravado por la multiplicidad de administraciones que se ven implicadas en el desarrollo de la Estación: Comunidad de Madrid, Junta de Castilla y León, Ayuntamientos de Cercedilla, Navacerrada, La Granja de San Ildefonso, Organismo Autónomo de Parques Nacionales, Parque Regional de la Cuenca Alta del Manzanares, dos Confederaciones Hidrográficas, dos demarcaciones de carreteras, y un sinfín de otras administraciones, desde el Ministerio de Defensa a la Comunidad de Ciudad y Tierra de Segovia, además de varios particulares. <br><br>

Fruto de dicha situación de crisis es la adquisición de TAGSA, que se encuentra prácticamente en situación de quiebra, por la Diputación Provincial de Madrid en 1981, pasando posteriormente al sector público de la Comunidad de Madrid en 1983, tras la creación de ésta.<br><br>


Tras un periodo de indecisión respecto al futuro de la Estación de Esquí de Puerto de Navacerrada, en 1992 se inician las primeras acciones de gestión de Deporte y Montaña de la Comunidad de Madrid S.A., nueva denominación de la sociedad, y se acuña el concepto de Estación de Montaña para el Puerto de Navacerrada. <br><br>

En 1994 se instala un sistema de producción de nieve y se inauguran 2 nuevos remontes en sustitución de anteriores instalaciones. Finalmente, en 1998 se inaugura el Telesilla Desembragable de Guarramillas con una capacidad de 2.400 personas/hora. Sin embargo, debido a distintas circunstancias, se van perdiendo progresivamente remontes y pistas hasta llegar a la actual situación de la Estación.<br><br>

Durante la temporada 2003-04 la Estación estuvo abierta 112 días y fue utilizada por 32.000 esquiadores, además de contar con 20.900 visitantes por la explotación en uso turístico de 2 remontes mecánicos.<br><br>

En la temporada 2005-06 la Estación de Esquí de Puerto de Navacerrada  funciona bajo mínimos y la Comunidad de Madrid decide finalmente su privatización.<br><br>


