De un tiempo a esta parte, los habitantes de La Granja no podemos quejarnos de las
dotaciones municipales para la pr&aacute;ctica del Deporte. <br><br> 

Disponemos del Pabellón Municipal de Deportes "SAN LUIS" y en Valsa&iacute;n se ha construido
el Polideportivo-frontón Municipal. <br><br>

Adem&aacute;s llevan varios a&ntilde;os funcionando las Escuelas Municipales con una variedad
deportiva bastante amplia.<br><br>

En mis a&ntilde;os mozos, disponiamos de La Pradera del Hospital (al lado del campo de f&uacute;tbol)
para jugar al f&uacute;tbol aquellos apasionantes derbys entre La Escuela y La Academia, (no
recuerdo los que habré jugado contra los chicos de La Escuela, aunque sólo recuerdo ganar 2 partidos,
y con un marcador bastante corto). Ellos nos ganaban, la mayor&iacute;a de las veces por 
aut&eacute;nticas goleadas. Esto es algo que curte el esp&iacute;ritu.<br> Así que despu&eacute;s 
de los partidos, que siempre terminaban con alguna que otra patada de m&aacute;s, nos ibamos Josete,
Oscar (Rinthin) y yo para el barrio (eramos casi vecinos), con otra derrota en nuestras mochilas. <br><br>

A&uacute;n recuerdo a Edu Juarez (que nos sacaba tres cabezas de alto a casi todos), a Juan Cocero y a 
Josito que nos daban un agua cuando querían. <br><br>

Bueno, que me disperso. Ten&iacute;amos La Pradera, el polideportivo, que no era m&aacute;s que una 
pista de cemento para jugar al f&uacute;tbol-sala o al baloncesto, cuando arreglaban las porter&iacute;as, 
ya que de las canastas nunca se supo, ... y para de contar.<br>

Cuando construyeron el nuevo Colegio Agapito Marazuela, saltabamos la valla para jugar en las pistas que
ten&iacute;a. <br><br>

Y ahora, las instalaciones son m&aacute;s numerosas. En realidad, a poco que tengamos ya es mejor que 
lo que ten&iacute;mos. <br>

M&aacute;s cosas que tenemos ahora, a ver si me acuerdo de todo..... <br>
<li> - Piscina Municipal (verano) </li>
<li> - Campos municipales de Fútbol (césped y tierra) </li>
<li> - Campo Municipal de Tiro con Arco </li>
<li> - Rocódromo Municipal de escalada </li>
<li> - Real Campo de Polo </li>
<li> - C.T. y alto Rendimiento de Escalada de Castilla y León </li>
<br><br>

Y tambi&eacute;n tenemos muy cerca:
<li> - la Estación de esquí de Navacerrada (Remontes, Pistas de nieve, Zona de nieve artificial...) para el invierno </li>
<li> - el Embalse del Río Eresma en el pontón (Piragüismo...) para el verano </li>
<li> - y todo el Pinar de Valsa&iacute;n, en cualquier estaci&oacute;n </li>


<br><br>
En estas cosas, es cuando se nota que un pueblo crece y est&aacute; vivo. En que se gasta m&aacute;s 
dinero en infraestructuras deportivas y culturales. Y que adem&aacute;s son utilizadas. <br><br>

As&iacute; que, el que no hace deporte en La Granja, es por que no quiere. :-)<br><br>

P.D. No olvidar tambi&eacute;n mi deporte favorito... los 100 metros barra (de bar)
